//> What is variable ?
// A variable is a named container used to store data. It acts as a placeholder that can hold different types of information, such as numbers, strings, objects, or even functions.

// To declare a variable in JS, we use 'var', 'let' or 'const' keyword, followed by the variable name. For Example:

var age;
let firstName;
const PI = 3.14;

// 'var' and 'let' are used for variables that can be reassigned with new values, while 'const' is used for variables that are meant to be constant and can not be reassigned once defined.

// Variables can also be assigned initial values at the time of declaration:

var payment = 123;
let lastName = "Mazumdar";
const N = 100;

// you can also assign values to var later in ypur code.

age = 25;
firstName = "Hossain";

// JS var's are dynamically typed. which means they can hold values of different types.
// For example, a variable can initially hold a number and later be reassigned to store a string:

let value = 21;
value = "hello";

// Variables in JavaScript have a lexical scope, meaning they are accessible only within the block or function they are declared in, unless they are declared with global scope using var outside of any function.

// Understanding variables is fundamental in programming, as they allow you to store and manipulate data, making your code more flexible and dynamic.
