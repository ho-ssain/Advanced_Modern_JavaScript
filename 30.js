//> iterable data structures
/**

In JavaScript, iterable data structures are objects that can be iterated over using loops or other iterable protocols, such as the for...of loop or the forEach() method. Here is a list of some common iterable data structures in JavaScript:

Arrays: Arrays are ordered collections of elements, and each element can be accessed by its index. They are one of the most commonly used iterable data structures in JavaScript.

Strings: Strings are sequences of characters, and each character in a string can be accessed by its index. Strings are iterable, allowing you to loop over each character.

Sets: Sets are collections of unique elements, meaning each element appears only once. They can be iterated over using the for...of loop or the forEach() method.

Maps: Maps are collections of key-value pairs. Both keys and values can be iterated over using the for...of loop or the forEach() method.

TypedArrays: Typed arrays are arrays that contain elements of a specific numeric data type, such as Int8Array, Uint8Array, Float32Array, etc. They are also iterable.

Arguments Object: The arguments object inside a function contains all the arguments passed to the function. It behaves like an array-like object and can be iterated over.

Generators: Generators are functions that can be paused and resumed, producing a sequence of values. They are iterable using the for...of loop.

DOM NodeLists: When querying the DOM using methods like querySelectorAll(), the returned NodeList is iterable.
 */
